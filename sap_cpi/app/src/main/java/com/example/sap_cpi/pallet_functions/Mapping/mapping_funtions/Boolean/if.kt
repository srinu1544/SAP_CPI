Function Name: if

Purpose:

if num1 > num2 = true
if num1 < num2 = false

note : if you want true value and false value take constants


Evaluates a condition and returns one of two values:
If condition is true, returns the true value.
If condition is false, returns the false value.

Input Parameters:
True value → Value to return if the condition is true.
Condition → Logical condition to evaluate.
False value → Value to return if the condition is false.

Example:
Condition: 10 > 5
True Value: "Greater"
False Value: "Smaller"
Output: "Greater"

Use Case in SAP CPI:

Conditional mapping logic.
Handling default values based on conditions.
Filtering and transforming data dynamically.








